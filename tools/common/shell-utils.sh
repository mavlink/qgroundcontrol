#!/usr/bin/env bash
# Shared shell utilities for QGC developer tools.
# Source this file at the top of shell scripts:
#   source "$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)/common/shell-utils.sh"

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

log_info()  { echo -e "${BLUE}[INFO]${NC} $*"; }
log_ok()    { echo -e "${GREEN}[OK]${NC} $*"; }
log_warn()  { echo -e "${YELLOW}[WARN]${NC} $*"; }
log_error() { echo -e "${RED}[ERROR]${NC} $*" >&2; }
